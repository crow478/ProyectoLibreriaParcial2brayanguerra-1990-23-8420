 private void calcularTotal() {
    // 1) Si hay una celda en edición, confirmar el valor antes de leer
    if (TablaDetalle.isEditing()) {
        TablaDetalle.getCellEditor().stopCellEditing();
    }

    double total = 0.0;
    int filas = TablaDetalle.getRowCount();

    for (int i = 0; i < filas; i++) {
        Object ocant = TablaDetalle.getValueAt(i, 3); // columna Cantidad
        Object oprec = TablaDetalle.getValueAt(i, 4); // columna Precio

        int cant = parseIntSafe(ocant);
        double precio = parseDoubleSafe(oprec);

        total += cant * precio;
    }

    // 👇 Aquí ya guardamos el total en la variable global
    tpagar = total;

    // 2) Muestra con 2 decimales en el cuadro de texto
    txtTotalPagar.setText(String.format(java.util.Locale.US, "%.2f", total));
}


// Acepta Integer, String, etc. y tolera null
private int parseIntSafe(Object val) {
    if (val == null) return 0;
    if (val instanceof Number) return ((Number) val).intValue();
    String s = val.toString().trim();
    if (s.isEmpty()) return 0;
    // Mantén solo dígitos y signo
    s = s.replaceAll("[^\\d-]", "");
    if (s.isEmpty()) return 0;
    try { return Integer.parseInt(s); } catch (NumberFormatException e) { return 0; }
}

private double parseDoubleSafe(Object val) {
    if (val == null) return 0.0;
    if (val instanceof Number) return ((Number) val).doubleValue();
    String s = val.toString().trim();
    if (s.isEmpty()) return 0.0;
    // Quita símbolos de moneda/espacios y convierte coma a punto
    s = s.replaceAll("[^\\d,.-]", "").replace(',', '.');
    if (s.isEmpty()) return 0.0;
    try { return Double.parseDouble(s); } catch (NumberFormatException e) { return 0.0; }
}

