void buscarProducto() {
    int r = 0;

    if (txtCodProducto.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "Debe ingresar un código de producto");
        return;
    }

    int id = Integer.parseInt(txtCodProducto.getText());

    // 👇 Aquí ya no declaramos "Modelo.Producto producto"
    // usamos el atributo global "producto"
    producto = pdao.listarID(id);

    if (producto != null && producto.getId() != 0) {
        txtproducto.setText(producto.getNombres());
        txtPrecio.setText(String.valueOf(producto.getPrecio()));
        txtstock.setText(String.valueOf(producto.getStock()));
    } else {
        int opcion = JOptionPane.showConfirmDialog(this,
                "Producto no registrado, ¿desea registrarlo?");
        txtCodProducto.requestFocus();

        if (opcion == JOptionPane.YES_OPTION) {
            Productos pf = new Productos(); 
            Principal.VentanaPrincipal.add(pf);
            pf.setVisible(true);
        }
    }
}


